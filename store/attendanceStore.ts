import { create } from 'zustand';

// Interfaces que reflejan exactamente la estructura SQLite
export interface Attendee {
  id: number;
  name: string;
  first_lastname: string;
  second_lastname: string;
  piime_id: string | null;
  created: string;
  updated: string;
}

export interface Event {
  id: number;
  name: string;
  date: string;
  created: string;
  updated: string;
}

export interface Attendance {
  id: number;
  event: number; // Foreign key to events.id
  attendee: number; // Foreign key to attendees.id
  created: string;
  updated: string;
}

// Tipos extendidos para la UI
export interface AttendeeWithPresence extends Attendee {
  isPresent: boolean;
}

export interface EventWithStats extends Event {
  attendanceCount: number;
  totalAttendees: number;
}

// Estado del store
interface AttendanceState {
  attendees: Attendee[];
  events: Event[];
  attendances: Attendance[];

  // Acciones para attendees
  addAttendee: (
    name: string,
    first_lastname: string,
    second_lastname: string,
    piime_id?: string
  ) => void;
  updateAttendee: (
    id: number,
    updates: Partial<Omit<Attendee, 'id' | 'created'>>
  ) => void;
  deleteAttendee: (id: number) => void;

  // Acciones para events
  addEvent: (name: string, date: string) => void;
  updateEvent: (
    id: number,
    updates: Partial<Omit<Event, 'id' | 'created'>>
  ) => void;
  deleteEvent: (id: number) => void;

  // Acciones para attendances
  toggleAttendance: (eventId: number, attendeeId: number) => void;
  setAttendance: (
    eventId: number,
    attendeeId: number,
    isPresent: boolean
  ) => void;

  // Métodos de consulta
  getEventById: (id: number) => Event | undefined;
  getAttendeeById: (id: number) => Attendee | undefined;
  getAttendanceForEvent: (eventId: number) => Attendance[];
  getAttendanceCount: (eventId: number) => number;
  isAttendeePresent: (eventId: number, attendeeId: number) => boolean;
  getAttendeesWithPresence: (eventId: number) => AttendeeWithPresence[];
  getEventsWithStats: () => EventWithStats[];
  getAttendeeFullName: (attendee: Attendee) => string;

  // Métodos para migración futura a SQLite
  initializeData: () => void;
  clearAllData: () => void;
  exportData: () => {
    attendees: Attendee[];
    events: Event[];
    attendances: Attendance[];
  };
  importData: (data: {
    attendees: Attendee[];
    events: Event[];
    attendances: Attendance[];
  }) => void;
}


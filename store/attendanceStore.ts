// Función para generar IDs únicos (en SQLite serían auto-increment)
const generateId = (): number => {
  return Date.now() + Math.floor(Math.random() * 1000);
};

export const useAttendanceStore = create<AttendanceState>()((set, get) => ({
  attendees: initialAttendees,
  events: initialEvents,
  attendances: initialAttendances,

  // Acciones para attendees
  addAttendee: (
    name: string,
    first_lastname: string,
    second_lastname: string,
    piime_id?: string
  ) => {
    const newAttendee: Attendee = {
      id: generateId(),
      name,
      first_lastname,
      second_lastname,
      piime_id: piime_id || null,
      created: new Date().toISOString(),
      updated: new Date().toISOString(),
    };
    set(state => ({ attendees: [...state.attendees, newAttendee] }));
  },

  updateAttendee: (
    id: number,
    updates: Partial<Omit<Attendee, 'id' | 'created'>>
  ) => {
    set(state => ({
      attendees: state.attendees.map(attendee =>
        attendee.id === id
          ? { ...attendee, ...updates, updated: new Date().toISOString() }
          : attendee
      ),
    }));
  },

  deleteAttendee: (id: number) => {
    set(state => ({
      attendees: state.attendees.filter(attendee => attendee.id !== id),
      attendances: state.attendances.filter(
        attendance => attendance.attendee !== id
      ),
    }));
  },

  // Acciones para events
  addEvent: (name: string, date: string) => {
    const newEvent: Event = {
      id: generateId(),
      name,
      date,
      created: new Date().toISOString(),
      updated: new Date().toISOString(),
    };
    set(state => ({ events: [newEvent, ...state.events] }));
  },

  updateEvent: (
    id: number,
    updates: Partial<Omit<Event, 'id' | 'created'>>
  ) => {
    set(state => ({
      events: state.events.map(event =>
        event.id === id
          ? { ...event, ...updates, updated: new Date().toISOString() }
          : event
      ),
    }));
  },

  deleteEvent: (id: number) => {
    set(state => ({
      events: state.events.filter(event => event.id !== id),
      attendances: state.attendances.filter(
        attendance => attendance.event !== id
      ),
    }));
  },

  // Acciones para attendances
  toggleAttendance: (eventId: number, attendeeId: number) => {
    const currentAttendance = get().attendances.find(
      attendance =>
        attendance.event === eventId && attendance.attendee === attendeeId
    );

    if (currentAttendance) {
      // Remover asistencia existente
      set(state => ({
        attendances: state.attendances.filter(
          attendance => attendance.id !== currentAttendance.id
        ),
      }));
    } else {
      // Crear nueva asistencia
      const newAttendance: Attendance = {
        id: generateId(),
        event: eventId,
        attendee: attendeeId,
        created: new Date().toISOString(),
        updated: new Date().toISOString(),
      };
      set(state => ({ attendances: [...state.attendances, newAttendance] }));
    }
  },

  setAttendance: (eventId: number, attendeeId: number, isPresent: boolean) => {
    const currentAttendance = get().attendances.find(
      attendance =>
        attendance.event === eventId && attendance.attendee === attendeeId
    );

    if (isPresent) {
      if (!currentAttendance) {
        // Crear nueva asistencia
        const newAttendance: Attendance = {
          id: generateId(),
          event: eventId,
          attendee: attendeeId,
          created: new Date().toISOString(),
          updated: new Date().toISOString(),
        };
        set(state => ({ attendances: [...state.attendances, newAttendance] }));
      }
    } else {
      if (currentAttendance) {
        // Remover asistencia existente
        set(state => ({
          attendances: state.attendances.filter(
            attendance => attendance.id !== currentAttendance.id
          ),
        }));
      }
    }
  },

  // Métodos de consulta
  getEventById: (id: number) => {
    return get().events.find(event => event.id === id);
  },

  getAttendeeById: (id: number) => {
    return get().attendees.find(attendee => attendee.id === id);
  },

  getAttendanceForEvent: (eventId: number) => {
    return get().attendances.filter(attendance => attendance.event === eventId);
  },

  getAttendanceCount: (eventId: number) => {
    return get().attendances.filter(attendance => attendance.event === eventId)
      .length;
  },

  isAttendeePresent: (eventId: number, attendeeId: number) => {
    return get().attendances.some(
      attendance =>
        attendance.event === eventId && attendance.attendee === attendeeId
    );
  },

  getAttendeesWithPresence: (eventId: number) => {
    const attendees = get().attendees;
    const attendeesWithPresence = attendees.map(attendee => ({
      ...attendee,
      isPresent: get().isAttendeePresent(eventId, attendee.id),
    }));

    // Ordenar por: 1) first_lastname, 2) second_lastname, 3) name
    return attendeesWithPresence.sort((a, b) => {
      // Comparar first_lastname
      const firstLastnameCompare = a.first_lastname.localeCompare(b.first_lastname, 'es', { 
        sensitivity: 'base',
        ignorePunctuation: true 
      });
      if (firstLastnameCompare !== 0) return firstLastnameCompare;

      // Si first_lastname es igual, comparar second_lastname
      const secondLastnameCompare = a.second_lastname.localeCompare(b.second_lastname, 'es', { 
        sensitivity: 'base',
        ignorePunctuation: true 
      });
      if (secondLastnameCompare !== 0) return secondLastnameCompare;

      // Si ambos apellidos son iguales, comparar name
      return a.name.localeCompare(b.name, 'es', { 
        sensitivity: 'base',
        ignorePunctuation: true 
      });
    });
  },

  getEventsWithStats: () => {
    const events = get().events;
    const totalAttendees = get().attendees.length;

    return events.map(event => ({
      ...event,
      attendanceCount: get().getAttendanceCount(event.id),
      totalAttendees,
    }));
  },

  getAttendeeFullName: (attendee: Attendee) => {
    return `${attendee.name} ${attendee.first_lastname} ${attendee.second_lastname}`.trim();
  },

  // Métodos para migración futura a SQLite
  initializeData: () => {
    set({
      attendees: initialAttendees,
      events: initialEvents,
      attendances: initialAttendances,
    });
  },

  clearAllData: () => {
    set({
      attendees: [],
      events: [],
      attendances: [],
    });
  },

  exportData: () => {
    const state = get();
    return {
      attendees: state.attendees,
      events: state.events,
      attendances: state.attendances,
    };
  },

  importData: (data: {
    attendees: Attendee[];
    events: Event[];
    attendances: Attendance[];
  }) => {
    set({
      attendees: data.attendees,
      events: data.events,
      attendances: data.attendances,
    });
  },
}));
